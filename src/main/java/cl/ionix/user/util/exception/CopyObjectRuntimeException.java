package cl.ionix.user.util.exception;

public class CopyObjectRuntimeException extends RuntimeException {
	
	private static final long serialVersionUID = -1043607981838920094L;

	public CopyObjectRuntimeException() {
		super();

	}

	public CopyObjectRuntimeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public CopyObjectRuntimeException(String message, Throwable cause) {
		super(message, cause);

	}

	public CopyObjectRuntimeException(String message) {
		super(message);

	}

	public CopyObjectRuntimeException(Throwable cause) {
		super(cause);

	}

}
